#!/usr/bin/env node
/**
 * granite/ait builds do not reliably inline `process.env.EXPO_PUBLIC_*`.
 * Materialize the API base URL from `.env` into a TS module before build/dev.
 */
import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(fileURLToPath(new URL('.', import.meta.url)), '..');
const envPath = join(root, '.env');
const outPath = join(root, 'src/shared/constants/apiBaseUrl.runtime.ts');

function readEnvFile(path) {
    const values = {};
    if (!existsSync(path)) {
        return values;
    }
    for (const line of readFileSync(path, 'utf8').split('\n')) {
        const trimmed = line.trim();
        if (!trimmed || trimmed.startsWith('#')) continue;
        const eq = trimmed.indexOf('=');
        if (eq < 0) continue;
        const key = trimmed.slice(0, eq).trim();
        let value = trimmed.slice(eq + 1).trim();
        if (
            (value.startsWith('"') && value.endsWith('"')) ||
            (value.startsWith("'") && value.endsWith("'"))
        ) {
            value = value.slice(1, -1);
        }
        values[key] = value;
    }
    return values;
}

const fromFile = readEnvFile(envPath);
const fromShell = (process.env.EXPO_PUBLIC_API_BASE_URL || '').trim();
const fromDotenv = (fromFile.EXPO_PUBLIC_API_BASE_URL || '').trim();
// Prefer `.env` so stale shell exports do not poison ait/Metro builds.
if (fromShell && fromDotenv && fromShell !== fromDotenv) {
    console.warn(
        `sync-api-base-url: shell EXPO_PUBLIC_API_BASE_URL=${fromShell} ignored; using .env=${fromDotenv}`,
    );
}
const raw = fromDotenv || fromShell || '';

const normalized = raw.replace(/\/$/, '');
const literal = JSON.stringify(normalized);

writeFileSync(
    outPath,
    `/** Generated by scripts/sync-api-base-url.mjs — do not edit by hand. */\n` +
        `export const API_BASE_URL: string = ${literal};\n`,
    'utf8',
);

console.log(
    normalized.length > 0
        ? `sync-api-base-url: API_BASE_URL=${normalized}`
        : 'sync-api-base-url: API_BASE_URL=(empty → mock)',
);
